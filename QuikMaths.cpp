#include "QuikMaths.h"

int FardUtil::MathUniverse::QuikMaths::Add2Nums(int num1, int num2)
{
    int result = num1 + num2;
    return result;
}

int FardUtil::MathUniverse::QuikMaths::Add3Nums(int num1, int num2, int num3)
{
    int result = num1 + num2 + num3;
    return result;
}
